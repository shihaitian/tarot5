export function generateReading(question: string, cards: TarotCard[]): string {
  const positionLabels = ['🌑 过去的影响', '🌓 当下的启示', '🌕 未来的指引'];
  const positionTexts = [
    (c: TarotCard) => c.past,
    (c: TarotCard) => c.present,
    (c: TarotCard) => c.future,
  ];

  let text = '';

  text += `亲爱的求问者，\n\n`;
  text += `你带着这样的疑问来到星辰面前：\n「${question}」\n\n`;
  text += `三张牌已从命运的长河中浮现，让我为你揭开它们的密语。\n\n`;

  for (let i = 0; i < 3; i++) {
    const card = cards[i];
    text += `━━━━━━━━━━━━━━━━━━━━\n`;
    text += `${positionLabels[i]}\n`;
    text += `${card.symbol} 【${card.name}】 ${card.nameEn}\n`;
    text += `关键词：${card.keywords.join(' · ')}\n\n`;
    text += `${positionTexts[i](card)}\n\n`;
  }

  text += `━━━━━━━━━━━━━━━━━━━━\n`;
  text += `✦ 综合指引 ✦\n\n`;
  text += generateSummary(question, cards);
  text += `\n\n愿星辰的光芒照亮你前行的道路。\n`;
  text += `✦ ── ❖ ── ✦`;

  return text;
}

function generateSummary(_question: string, cards: TarotCard[]): string {
  const positiveIds = new Set([0, 1, 3, 6, 8, 17, 19, 21]);
  const challengeIds = new Set([13, 15, 16, 18]);

  const positiveCount = cards.filter(c => positiveIds.has(c.id)).length;
  const challengeCount = cards.filter(c => challengeIds.has(c.id)).length;

  let summary = '';

  summary += `纵观这三张牌——${cards[0].name}、${cards[1].name}与${cards[2].name}——它们共同编织了一幅关于你当下处境的图景。\n\n`;

  if (positiveCount >= 2) {
    summary += `整体能量非常积极。从过去的${cards[0].name}所代表的${cards[0].keywords[0]}，到当下${cards[1].name}带来的${cards[1].keywords[0]}，再到未来${cards[2].name}许诺的${cards[2].keywords[0]}——宇宙正以温暖而坚定的方式引导着你。\n\n`;
    summary += `你正行走在一条被光芒照耀的道路上。保持你的信心和热忱，因为星辰的排列正在为你创造有利的条件。你内心的声音是正确的——跟随它，你将到达心之所向。`;
  } else if (challengeCount >= 2) {
    summary += `这组牌阵揭示了一段需要勇气和智慧的旅程。${cards[0].name}提醒你审视过去的${cards[0].keywords[0]}，${cards[1].name}呼唤你在当下面对${cards[1].keywords[0]}，而${cards[2].name}则指引你走向${cards[2].keywords[0]}。\n\n`;
    summary += `虽然前方的道路可能有些曲折，但请记住——每一次挑战都是灵魂成长的契机。宇宙不会给你无法承受的考验。在风暴中保持内心的平静，在黑暗中信任指引你的那束光。穿越这段旅程后，你将变得更加强大和智慧。`;
  } else {
    summary += `这三张牌展现了一条从认知到行动、从回望到前瞻的平衡道路。${cards[0].name}为你的过去提供了关于${cards[0].keywords[0]}的理解，${cards[1].name}照亮了当下${cards[1].keywords[0]}的重要性，而${cards[2].name}则为未来描绘了${cards[2].keywords[0]}的可能。\n\n`;
    summary += `这是一组充满洞见的牌阵。宇宙提醒你在前行中保持内外的平衡——既要倾听理性的分析，也要信任直觉的低语。你正处在一个微妙而重要的节点上，你做出的每一个选择都在塑造你的未来。用觉知和慈悲引导自己，你会找到属于你的答案。`;
  }

  return summary;
}
